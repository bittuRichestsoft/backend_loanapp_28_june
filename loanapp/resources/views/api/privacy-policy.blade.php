POLITICA DE PRIVACIDADE DA CAPITAL

A presente Política de Privacidade destina-se a ajudá-lo a compreender as informações que recolhemos e o motivo para o fazermos.

A Capital não partilha a informação de utlizadores com terceiros, apenas recolhe informações para prestar um melhor serviço a todos os utilizadores de formas a mitigar o risco no acto da conceção de emprestimos de um utilizador para outro e fornecer uma forma mais funcional e rápida na aquisição de capital financeiro.

Ao fazer o download e uso da Capital  reconhece que leu, concordou e aceitou a politica de privacidade, incluindo a seguinte declaração:

Dou o meu consentimento para a recolha e processamento das minhas informações pessoais para fins comerciais legítimos, incluindo, mas não se limitando a, determinar minha identidade e o meu rating de crédito para fornecer um empréstimo. Certifico por meio deste que todas as informações fornecidas por mim são verdadeiras e corretas, tanto quanto é do meu conhecimento, e que qualquer deturpação ou falsidade nelas contida será considerada como um ato de fraude a Capital  e aos seus parceiros.





