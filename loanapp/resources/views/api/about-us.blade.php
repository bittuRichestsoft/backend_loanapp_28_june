Este é um acordo de serviços tecnologicos entre o utilizador e a CR-CAPITAL, uma empresa organizada e registrada sob as leis da República de Angola que fornece a aplicação móvel Capital.
 
A  Capital serve de interligação que permite ao utilizador dar ou receber emprestimos, o uso desta aplicação é limitada a maiores de 18 anos, o limite máximo de emprestimos varia de acordo com a pontuação de crédito ,do histórico de reembolso e outros fatores. Os encargos associados aos emprestimos incluem uma taxa de juro que varia de acordo com a data de termo do emprestimo e uma taxa de serviço.

A Capital não se responsabiliza por qualquer emprestimo não devolvido, apenas fornece a melhor forma de conseguir emprestimos unindo as duas partes interessadas e mitigando o nivel de risco por meio do processo de rating de credito.

