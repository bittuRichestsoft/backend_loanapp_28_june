A capital é uma aplicação móvel que interligado os seus utilizadores e fornece dois tipos de serviços, nomeadamente:

Pedir um Empréstimo : funcionalidade que permite solicitar um empréstimo a qualquer utilizador da comunidade da capital denominada Marketplace ou solicitar um empréstimo directamente a um amigo.

Dar um Empréstimo: funcionalidade que permite oferecer um empréstimo a qualquer utilizador da comunidade da capital denominada Marketplace ou solicitar um empréstimo directamente a um amigo.

